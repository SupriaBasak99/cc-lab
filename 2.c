#include<stdio.h>
int main()
{
	int num1,num2;
	printf("enter the first no:");
	scanf("%d",&num1);
	printf("enter the 2nd number:");
	scanf("%d",&num2);
	printf("%d+%d=%d",num1,num2,num1+num2);
	printf("%d-%d=%d",num1,num2,num1-num2);
	printf("%d*%d=%d",num1,num2,num1*num2);
	printf("%d/%d=%d",num1,num2,num1/num2);
	return 0;
	
}
